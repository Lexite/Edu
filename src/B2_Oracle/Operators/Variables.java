package Learning.FirstLearnign.Operators;

class PrePostDemo {
    public static void main(String[] args) {
        int i = 0;
        i++;
        System.out.println(i);
        ++i;
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i++);
        System.out.println(i);
    }
}


